public class Elemento {

    private int codigoElemento;
    private int codigoOrdenServicio;
    private String descripcion;
    private String estado;
    private String marca;
    private String modelo;
    private String serial;
    private String tipo;

    public Elemento() {
    }

    public int getCodigoElemento() {
        return codigoElemento;
    }

    public void setCodigoElemento(int codigoElemento) {
        this.codigoElemento = codigoElemento;
    }

    public int getCodigoOrdenServicio() {
        return codigoOrdenServicio;
    }

    public void setCodigoOrdenServicio(int codigoOrdenServicio) {
        this.codigoOrdenServicio = codigoOrdenServicio;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public String getSerial() {
        return serial;
    }

    public void setSerial(String serial) {
        this.serial = serial;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    
   
}
