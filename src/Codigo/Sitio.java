package Codigo;

public class Sitio {

	private int idSitio;
	private String nombreSitio;
	private String ubicacionSitio;
	private int idPadreSitio;
	public int getIdSitio() {
		return idSitio;
	}
	public void setIdSitio(int idSitio) {
		this.idSitio = idSitio;
	}
	public String getNombreSitio() {
		return nombreSitio;
	}
	public void setNombreSitio(String nombreSitio) {
		this.nombreSitio = nombreSitio;
	}
	public String getUbicacionSitio() {
		return ubicacionSitio;
	}
	public void setUbicacionSitio(String ubicacionSitio) {
		this.ubicacionSitio = ubicacionSitio;
	}
	public int getIdPadreSitio() {
		return idPadreSitio;
	}
	public void setIdPadreSitio(int idPadreSitio) {
		this.idPadreSitio = idPadreSitio;
	}
	
	
	
}
